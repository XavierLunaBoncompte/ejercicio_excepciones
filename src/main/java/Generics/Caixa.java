package Generics;

/**
 *
 * @author lubo1
 */
public class Caixa {

    private Object content;

    public Caixa(Object content) {
        this.content = content;
    }

    public Object getContent() {
        return content;
    }

    public void setContent(Object content) {
        this.content = content;
    }

    @Override
    public String toString() {
        return "Caixa{" + "content=" + content + '}';
    }

}
